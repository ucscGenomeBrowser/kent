#!/bin/bash

# workflowMonitor.sh - check a pending Galaxy kegAlign workflow invocation
#   and install results when complete
#
# usage: workflowMonitor.sh <reqId> <buildDir>
#
# Called periodically (e.g. from cron) to poll the Galaxy API for
# invocation status.  Exits 0 silently when still running so cron
# can simply re-invoke on schedule.  When complete, downloads results
# via planemo and installs chain/liftOver bigBed files into the build
# and swap directories.
#
# Expects in <buildDir>:
#   pendingInvocationId.txt  - written by kegAlign.sh
#                        (3 tab-separated fields: DS invocationId logJsonPath)
#   kegAlign.sh              - generated by kegAlignLastz.sh,
#                              variable definitions sourced for buildDir,
#                              swapDir, PM, targetDb, queryDb, etc.
# status indicators:
#   0 pending, 1 notified, 2 in progress, 3 galaxy done, 4 tracks complete,
#   5 finish notification, 6 complete, 7 problems

set -eEu -o pipefail

if [ $# != 2 ]; then
  printf "usage: workflowMonitor.sh <reqId> <buildDir>\n" 1>&2
  exit 255
fi

export reqId="$1"
export buildDir="$2"
export successCount=0
export scriptDir=$(cd "$(dirname "$0")" && pwd)
export centDb="hgcentral"
export hgSql="hgsql -hgenome-centdb"

##############################################################################
### errors - set error status in the table
function setErrorStatus() {
  id="${1}"
  /cluster/bin/x86_64/${hgSql} -N -e \
      "UPDATE ottoRequest SET status=7 WHERE id=${id};" "${centDb}"
}
##############################################################################

if [ ! -d "${buildDir}" ]; then
  printf "ERROR: buildDir not found: %s\n" "${buildDir}" 1>&2
  exit 255
fi

cd "${buildDir}"

# already finished?
if [ -s successInvocationId.txt ]; then
  printf "# already completed: %s\n" "${buildDir}" 1>&2
  exit 0
fi

# no pending invocation?
if [ ! -s pendingInvocationId.txt ]; then
  printf "ERROR: no pendingInvocationId.txt in %s\n" "${buildDir}" 1>&2
  setErrorStatus ${reqId}
  exit 255
fi

# read pending invocation fields
export DS=$(cut -f1 pendingInvocationId.txt)
export invocationId=$(cut -f2 pendingInvocationId.txt)
export logJson=$(cut -f3 pendingInvocationId.txt)

# source variable definitions from the generated kegAlign.sh
if [ ! -s kegAlign.sh ]; then
  printf "ERROR: no kegAlign.sh in %s\n" "${buildDir}" 1>&2
  setErrorStatus ${reqId}
  exit 255
fi
source <(grep -E '^export (swapDir|PM|targetDb|queryDb|QueryDb|Target|tSizes|qSizes)=' kegAlign.sh)

# printf "# monitoring invocation: %s\n" "${invocationId}" 1>&2
# printf "# buildDir: %s\n" "${buildDir}" 1>&2

# read Galaxy credentials from planemo profile
export profileJson="${HOME}/.planemo/profiles/vgp/planemo_profile_options.json"
if [ ! -s "${profileJson}" ]; then
  printf "ERROR: planemo profile not found: %s\n" "${profileJson}" 1>&2
  setErrorStatus ${reqId}
  exit 255
fi

# galaxyState.py reads the profile and prints: pending | complete | failed
state=$("${scriptDir}/galaxyState.py" "${profileJson}" "${invocationId}")
case "${state}" in
  pending)  exit 0 ;;
  complete) ;;   # fall through to download
  failed)   setErrorStatus ${reqId} ; exit 255 ;;
  *)        printf "ERROR: unexpected galaxyState.py output: %q\n" \
              "${state}" 1>&2
            setErrorStatus ${reqId}
            exit 255
            ;;
esac

# printf "# all jobs complete, downloading results\n" 1>&2
/cluster/bin/x86_64/${hgSql} -N -e \
      "UPDATE ottoRequest SET status = 3 WHERE id = ${reqId};" "${centDb}"

############################################################################
# download results via planemo
############################################################################
mkdir -p "result/${DS}"
### see if this has already happened
export resultCount=0
while read -r F; do
  if [ -s "${F}" ]; then
     resultCount=$((resultCount+1))
  fi
done < <(ls  result/${DS}/* 2> /dev/null)

if [ "${resultCount}" -lt 6 ]; then
 { time "${PM}" invocation_download "${invocationId}" --profile vgp \
      --output_directory "result/${DS}" ; } >> galaxyDownload.log 2>&1
else
  printf "download previously completed\n" 1>&2
fi
### verify it worked
resultCount=0
while read -r F; do
  if [ -s "${F}" ]; then
     resultCount=$((resultCount+1))
  fi
done < <(ls  result/${DS}/*)

if [ "${resultCount}" -lt 6 ]; then
  printf "ERROR: planemo invocation_download failed\n" 1>&2
  setErrorStatus ${reqId}
  exit 255
fi

############################################################################
# quickChain - construct the quick.bb files
# args: target query liftOver sizes fbFile
############################################################################
function quickChain() {
  local target=$1
  local query=$2
  local liftOver=$3
  local sizesFile=$4
  local fbFile=$5
  local chainName="${target}.${query}.quick"
  /cluster/bin/x86_64/chainSwap "${liftOver}" stdout | /cluster/bin/x86_64/chainToBigChain stdin \
    ${target}.${query}.quick.chain.tab ${target}.${query}.quick.link.tab
  /cluster/bin/x86_64/bedToBigBed -type=bed6+6 -as=$HOME/kent/src/hg/lib/bigChain.as -tab \
    ${target}.${query}.quick.chain.tab ${sizesFile} ${chainName}.bb > /dev/null 2>&1
  /cluster/bin/x86_64/bedToBigBed -type=bed4+1 -as=$HOME/kent/src/hg/lib/bigLink.as -tab \
    ${target}.${query}.quick.link.tab ${sizesFile} ${chainName}Link.bb > /dev/null 2>&1
  rm -f ${target}.${query}.quick.chain.tab ${target}.${query}.quick.link.tab
  local totalBases=$(/cluster/bin/x86_64/ave -col=2 ${sizesFile} | grep "^total" | awk '{printf "%d", $2}')
  local basesCovered=$(/cluster/bin/x86_64/bigBedInfo ${chainName}Link.bb | grep "basesCovered" | cut -d' ' -f2 | tr -d ',')
  local percentCovered=$(echo ${basesCovered} ${totalBases} | awk '{printf "%.3f", 100.0*$1/$2}')
  printf "%d bases of %d (%s%%) in intersection\n" \
    "${basesCovered}" "${totalBases}" "${percentCovered}" > ${fbFile}
}

############################################################################
# chainBigBedFb - convert chain to bigBed and compute featureBits
# args: db chainName chainGz sizesFile fbFile
############################################################################
function chainBigBedFb() {
  local db=$1
  local chainName=$2
  local chainGz=$3
  local sizesFile=$4
  local fbFile=$5
  /cluster/bin/x86_64/chainToBigChain "${chainGz}" ${chainName}.tab ${chainName}Link.tab
  /cluster/bin/x86_64/bedToBigBed -type=bed6+6 -as=$HOME/kent/src/hg/lib/bigChain.as -tab \
    ${chainName}.tab ${sizesFile} ${chainName}.bb > /dev/null 2>&1
  /cluster/bin/x86_64/bedToBigBed -type=bed4+1 -as=$HOME/kent/src/hg/lib/bigLink.as -tab \
    ${chainName}Link.tab ${sizesFile} ${chainName}Link.bb > /dev/null 2>&1
  rm -f ${chainName}.tab ${chainName}Link.tab chain.tab link.tab
  local totalBases=$(/cluster/bin/x86_64/ave -col=2 ${sizesFile} | grep "^total" | awk '{printf "%d", $2}')
  local basesCovered=$(/cluster/bin/x86_64/bigBedInfo ${chainName}Link.bb | grep "basesCovered" | cut -d' ' -f2 | tr -d ',')
  local percentCovered=$(echo ${basesCovered} ${totalBases} | awk '{printf "%.3f", 100.0*$1/$2}')
  printf "%d bases of %d (%s%%) in intersection\n" \
    "${basesCovered}" "${totalBases}" "${percentCovered}" > ${fbFile}
}

############################################################################
# bumpIf <file>  -- increment successCount if file exists and is non-empty
############################################################################
function bumpIf() {
  if [ -s "$1" ]; then
    successCount=$((successCount+1))
  fi
}

############################################################################
# unzipResult <srcGlob> <dstGz>
#   gzip a planemo result file into dstGz if not already present, then
#   bump successCount when the destination ends up populated
############################################################################
function unzipResult() {
  local srcGlob=$1
  local dstGz=$2
  if [ ! -s "${dstGz}" ]; then
    local src=$(ls ${srcGlob})
    gzip -c "${src}" > "${dstGz}"
  fi
  bumpIf "${dstGz}"
}

############################################################################
# buildChainBb <fn> <workDir> <db> <chainName> <chainGz> <sizes> <fbFile>
#   cd to workDir (so the .tab/.bb intermediates land beside the chain),
#   run fn (chainBigBedFb or quickChain) if fbFile missing, then bump
#   successCount and cat the fbFile to stderr
############################################################################
function buildChainBb() {
  local fn=$1 workDir=$2
  local db=$3 chainName=$4 chainGz=$5 sizes=$6 fbFile=$7
  if [ ! -s "${fbFile}" ]; then
    cd "${workDir}"
    "${fn}" "${db}" "${chainName}" "${chainGz}" "${sizes}" "${fbFile}"
  fi
  bumpIf "${fbFile}"
# cat "${fbFile}" 1>&2
}

############################################################################
# install all chain.gz files and their bigBed/featureBits derivatives
############################################################################
mkdir -p "${buildDir}/axtChain" "${swapDir}/axtChain"

# planemo result -> chain.gz install table
# fields: srcGlob | dstGz
for row in \
  "${buildDir}/result/${DS}/allChain__*.chain|${buildDir}/axtChain/${targetDb}.${queryDb}.all.chain.gz" \
  "${buildDir}/result/${DS}/liftOverChain_*.chain|${buildDir}/axtChain/${targetDb}.${queryDb}.over.chain.gz" \
  "${buildDir}/result/${DS}/allChainSwap_*.chain|${swapDir}/axtChain/${queryDb}.${targetDb}.all.chain.gz" \
  "${buildDir}/result/${DS}/swapLiftOverChain_*.chain|${swapDir}/axtChain/${queryDb}.${targetDb}.over.chain.gz"
do
  IFS="|" read -r srcGlob dstGz <<< "${row}"
  unzipResult "${srcGlob}" "${dstGz}"
done

# chain.gz -> bigBed + featureBits build table
# fields: fn | workDir | db | chainName | chainGz | sizes | fbFile
for row in \
  "chainBigBedFb|${buildDir}/axtChain|${targetDb}|chain${QueryDb}|${buildDir}/axtChain/${targetDb}.${queryDb}.all.chain.gz|${tSizes}|${buildDir}/fb.${targetDb}.chain${QueryDb}Link.txt" \
  "chainBigBedFb|${buildDir}/axtChain|${targetDb}|chainLiftOver${QueryDb}|${buildDir}/axtChain/${targetDb}.${queryDb}.over.chain.gz|${tSizes}|${buildDir}/fb.${targetDb}.chainLiftOver${QueryDb}Link.txt" \
  "quickChain|${buildDir}/axtChain|${targetDb}|${queryDb}|${buildDir}/axtChain/${targetDb}.${queryDb}.over.chain.gz|${qSizes}|${buildDir}/fb.${targetDb}.quick${QueryDb}Link.txt" \
  "chainBigBedFb|${swapDir}/axtChain|${queryDb}|chain${Target}|${swapDir}/axtChain/${queryDb}.${targetDb}.all.chain.gz|${qSizes}|${swapDir}/fb.${queryDb}.chain${Target}Link.txt" \
  "chainBigBedFb|${swapDir}/axtChain|${queryDb}|chainLiftOver${Target}|${swapDir}/axtChain/${queryDb}.${targetDb}.over.chain.gz|${qSizes}|${swapDir}/fb.${queryDb}.chainLiftOver${Target}Link.txt" \
  "quickChain|${swapDir}/axtChain|${queryDb}|${targetDb}|${swapDir}/axtChain/${queryDb}.${targetDb}.over.chain.gz|${tSizes}|${swapDir}/fb.${queryDb}.quick${Target}Link.txt"
do
  IFS="|" read -r fn workDir a1 a2 a3 a4 a5 <<< "${row}"
  buildChainBb "${fn}" "${workDir}" "${a1}" "${a2}" "${a3}" "${a4}" "${a5}"
done

############################################################################
# mark complete
############################################################################
### verify all the actual files were created
cd "${buildDir}"
if [ "${successCount}" -ne 10 ]; then
  printf "ERROR: workflowMonitor.sh did not create 10 result files ?\n" 1>&2
  setErrorStatus ${reqId}
  exit 255
fi

printf "%s\t%s\t%s\n" "${DS}" "${invocationId}" "${logJson}" \
  > successInvocationId.txt
rm -f pendingInvocationId.txt

# printf "### workflow monitor complete: %s %s -> %s\n" \
#   "${buildDir}" "${targetDb}" "${queryDb}" 1>&2
